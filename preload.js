const{contextBridge:cb,ipcRenderer:ip}=require("electron");
cb.exposeInMainWorld("api",{
hasApiKey:()=>ip.invoke("hasApiKey"),
setApiKey:(k)=>ip.invoke("setApiKey",k),
validateKey:(k)=>ip.invoke("validateKey",k),
getStatus:()=>ip.invoke("getStatus"),
search:(q)=>ip.invoke("search",q),
getStreamUrl:(u)=>ip.invoke("getStreamUrl",u),
getDownloadUrl:(u,f)=>ip.invoke("getDownloadUrl",u,f),
downloadFile:(u,n)=>ip.invoke("downloadFile",u,n),
openExternal:(u)=>ip.invoke("openExternal",u),
getHistory:()=>ip.invoke("getHistory"),
addHistory:(i)=>ip.invoke("addHistory",i),
removeHistory:(id)=>ip.invoke("removeHistory",id),
clearHistory:()=>ip.invoke("clearHistory"),
openFile:(p)=>ip.invoke("openFile",p),
showInFolder:(p)=>ip.invoke("showInFolder",p),
isDownloaded:(u)=>ip.invoke("isDownloaded",u),
checkFileExists:(p)=>ip.invoke("checkFileExists",p),
notify:(t,b)=>ip.invoke("notify",t,b),
onDownloadProgress:(cb)=>ip.on("download-progress",(_e,p)=>cb(p)),
onAppUpdated:(cb)=>ip.on("app-updated",(_e,m)=>cb(m)),
onApiStatus:(cb)=>ip.on("api-status",(_e,s)=>cb(s)),
minimize:()=>ip.send("window-minimize"),
maximize:()=>ip.send("window-maximize"),
close:()=>ip.send("window-close"),
quit:()=>ip.invoke("quitApp"),
});
